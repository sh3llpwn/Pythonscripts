x = 15
y = 4

print("x = {} and y = {}".format(x,y))
print("\n")

# addition
z = x+y
print("Addition x+y =",z)  # print(x+y)
print("\n")

# subtraction
z = x-y
print("Subtraction x-y =",z)
print("\n")

# multiplication
z = x*y
print("Multiplication x*y =",z)
print("\n")

# division
z = x/y
print("Division x/y =",z)
print("\n")

# quotient
z = x//y
print("Quotient x//y =",z)
print("\n")

# remainder
z = x%y
print("Remainder x%y =",z)
print("\n")

# increment
z = x
print("Initial z =",z)
z = z +1
z +=1		#short hand
print("Twice incremented z =",z)

print("""\n similarly you can short hand other operations like :
		i = i - 2  ---->  i -= 2
		i = i / 2  -----> i /= 2
		i = i * 2  ---->  i *= 2
	""")
